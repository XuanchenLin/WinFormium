namespace WinFormium;

/// <summary>
/// Specifies the system color mode for the application.
/// </summary>
public enum SystemColorMode
{
    /// <summary>
    /// Automatically selects the color mode based on the system or user preference.
    /// </summary>
    Auto,
    /// <summary>
    /// Uses the light color mode.
    /// </summary>
    Light,
    /// <summary>
    /// Uses the dark color mode.
    /// </summary>
    Dark,
}

